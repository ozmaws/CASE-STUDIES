class SavingsAccount:
    RATE = 0.02    
    def __init__(self, name, pin, balance = 0.0):
        self.name = name
        self.pin = pin
        self.balance = balance
    def __str__(self):
        result =  'Name:    ' + self.name + '\n' 
        result += 'PIN:     ' + self.pin + '\n' 
        result += 'Balance: ' + str(self.balance)
        return result
    def getBalance(self):  
        return self.balance
    def getName(self):
        return self.name
    def getPin(self):
        return self.pin
    def deposit(self, amount):
        self.balance += amount
        return None
    def withdraw(self, amount):
        if amount < 0:
            return "Total must be at least 0 or greater"
        elif self.balance < amount:
            return "Insufficient"
        else:
            self.balance -= amount
            return None
    def computeInterest(self):
        interest = self.balance * SavingsAccount.RATE
        self.deposit(interest)
        return interest

